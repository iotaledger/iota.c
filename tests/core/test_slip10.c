void test_derive_key_from_path() {
  size_t test_cases = sizeof(slip10_set) / sizeof(test_slip10_t);
  for (size_t i = 0; i < test_cases; i++) {
    slip10_key_t key = {};
    byte_t pub_key[SLIP10_PUBLIC_KEY_BYTES] = {};

    printf("slip10: %s, %s\n", slip10_set[i].seed, slip10_set[i].path);
    size_t seed_len = strlen(slip10_set[i].seed) / 2;
    byte_t *b_seed = malloc(seed_len);
    if (b_seed) {
      // hex seed to bin seed
      TEST_ASSERT(hex2bin(slip10_set[i].seed, strlen(slip10_set[i].seed), b_seed, seed_len) == 0);

      // key derivation
      int ret = slip10_key_from_path(b_seed, seed_len, slip10_set[i].path, ED25519_CURVE, &key);
      TEST_ASSERT(ret == slip10_set[i].err);

      if (ret == 0) {
        // validating chain code
        TEST_ASSERT_EQUAL_MEMORY(slip10_set[i].chain_code, key.chain_code, SLIP10_CHAIN_CODE_BYTES);
        // validating private key
        TEST_ASSERT_EQUAL_MEMORY(slip10_set[i].private, key.key, SLIP10_PRIVATE_KEY_BYTES);
        // validating public key
        TEST_ASSERT(slip10_public_key(ED25519_CURVE, &key, pub_key) == 0);
        TEST_ASSERT_EQUAL_MEMORY(slip10_set[i].public, pub_key, SLIP10_PUBLIC_KEY_BYTES);
      }
    }
    free(b_seed);
    b_seed = NULL;
  }
}

